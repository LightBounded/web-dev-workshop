// Arithmetic: +, -, *, **, /, %, ++, --
// Assignment: +=, -=, *=, **=, /=, %=

let a = 6;
let b = 4;

console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a ** b); // 6^4
console.log(a / b);
console.log(a % b);
console.log(++a);
console.log(--b);

a = 100;
b += 6;
