// there are only 4 data types in JS: string, number, boolean, object

let a = "Hello, World!"; // typeof a = string
let b = 5; // typeof b = number
let c = true; // typeof c = boolean (must be either true or false)

let d = {}; // typeof d = object
let e = []; // typeof e = object

// Note that objects and arrays are of type object
